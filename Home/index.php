                <p>
                    The 2019 Cricket World Cup (officially ICC Cricket World Cup 2019) is the 12th 
                    edition of the Cricket World Cup, scheduled to be hosted by England and Wales, 
                    from 30 May to 14 July 2019.
                </p>
                <p>
                    The hosting rights were awarded in April 2006, after England and Wales withdrew 
                    from the bidding to host the 2015 ICC Cricket World Cup, which was held in Australia
                    and New Zealand. The first match will be played at The Oval while the final will 
                    be played at Lord's. It is the fifth time that the Cricket World Cup will be 
                    held in England and Wales, following the 1975, 1979, 1983 and 1999 World Cups.
                </p>
                <p>
                    The format for the tournament will be a single group of 10 teams, with each team 
                    playing the other nine once, and the top four at the end of the group phase 
                    progressing to the semi-finals. The 10-team tournament has gained criticism due to 
                    the lack of Associate teams in the tournament. Given the increase of the 
                    Test-playing nations from 10 to 12, with the admission of Ireland and Afghanistan 
                    in June 2017, it will be the first World Cup to be contested without all of the 
                    Test playing nations being present, and after the elimination of all the 
                    Associate teams at the qualifying tournament, this will be also be the first World 
                    Cup to feature no Associate members.
                </p>
                <p>
                    Following the 2019 Pulwama attack, several former Indian players and the Board of 
                    Control for Cricket in India (BCCI) called for the boycott of the group match 
                    fixture between India and Pakistan, wanting to ban the Pakistan team from playing 
                    in the tournament. However, after conducting a press meet in Dubai, the 
                    International Cricket Council (ICC) rejected the BCCI's statement regarding banning 
                    Pakistan from the World Cup and assured that the scheduled match will go ahead as 
                    planned despite the ongoing standoff between the two nations.
